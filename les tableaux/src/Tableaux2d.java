public class Tableaux2d {

	public static void main(String[] args) {
		int mat[][] = {{1,5,10,15},{2,4,6,8},{10,20,30,40}};
		//affichage des elts de la mat
		for (int i=0;i<3;i++){
			for (int j=0;j<4;j++){
				System.out.print(mat[i][j]+"\t");
			}
			System.out.println();
		}
		}

	}


